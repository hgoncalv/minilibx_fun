#include "../libft.h"

int	ft_isnbr(char *c)
{
	if (*c == '-' || *c == '+')
		c++;
	if (!*c)
		return (0);
	while (*c)
	{
		if (!(ft_isdigit(*c)))
			return (0);
		c++;
	}
	return (1);
}
